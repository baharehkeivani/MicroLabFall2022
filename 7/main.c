void createChars(){
	createChar(0,locked);
	createChar(1,unlocked);
}

int check_for_pass_correctness(){
  //if correct return 1 else 0
  if (prevVolume - currVolume == passcode[passIndex]){
    return 1;
  }
  return 0;
}

int normalizeVolume(int volume){
   int temp1 = volume/10 ;
   int temp2 = (volume%10) / 5;
   return temp1 * 10 + temp2 * 5;
}

void display_on_screen(){
  switch(state){
    case 0:
    	clear();
		setCursor(1, 1);
		print("Welcome to SafeBox");
		break;
    case 1:
    	clear();
		setCursor(3, 1);
		print("Enter pass ");
		write(0);
		int x = 0;
		int col = 2;
		while(x < passIndex){
			HAL_GPIO_WritePin(GPIOE, GPIO_PIN_9, 1);
			setCursor(col, 3);
			char *str = (char*)malloc(13 * sizeof(char));
			sprintf(str, "%d", passcode[x]);
			print(str);
			free(str);
			x++;
			col +=4 ;
		}
		break;
    case 2:
    	clear();
    	setCursor(1, 1);
		print("SafeBox Unlocked ");
		write(1);
      break;
    case 3:
    	clear();
    	setCursor(5, 1);
		print("Wrong pass");
		setCursor(5, 2);
		print("Try again!");
      break;
  }
}
void update_pass_live(){
	setCursor(16, 3);
	print("   ");
	setCursor(16, 3);
	int liveEnteredVol = prevVolume - currVolume;
	char *str = (char*)malloc(13 * sizeof(char));
	sprintf(str, "%d", liveEnteredVol);
	print(str);
	free(str);
}

void PWM_Start()
{
    HAL_TIM_PWM_Start(pwm_timer, pwm_channel);
}

void PWM_Change_Tone(uint16_t pwm_freq, uint16_t volume) // pwm_freq (1 - 20000), volume (0 - 1000)
{
    if (pwm_freq == 0 || pwm_freq > 20000)
    {
        __HAL_TIM_SET_COMPARE(pwm_timer, pwm_channel, 0);
    }
    else
    {
        const uint32_t internal_clock_freq = HAL_RCC_GetSysClockFreq();
        const uint16_t prescaler = 1 + internal_clock_freq / pwm_freq / 60000;
        const uint32_t timer_clock = internal_clock_freq / prescaler;
        const uint32_t period_cycles = timer_clock / pwm_freq;
        const uint32_t pulse_width = volume * period_cycles / 1000 / 2;

        pwm_timer->Instance->PSC = prescaler - 1;
        pwm_timer->Instance->ARR = period_cycles - 1;
        pwm_timer->Instance->EGR = TIM_EGR_UG;
        __HAL_TIM_SET_COMPARE(pwm_timer, pwm_channel, pulse_width); // pwm_timer->Instance->CCR2 = pulse_width;
    }
}

void Change_Melody(const Tone *melody, uint16_t tone_count)
{
    melody_ptr = melody;
    melody_tone_count = tone_count;
    current_tone_number = 0;
}

void Update_Melody()
{
    if ((HAL_GetTick() > current_tone_end) && (current_tone_number < melody_tone_count))
    {
        const Tone active_tone = *(melody_ptr + current_tone_number);
        PWM_Change_Tone(active_tone.frequency, volume);
        current_tone_end = HAL_GetTick() + active_tone.duration;
        current_tone_number++;
    }
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){
  if(last_interrupt + 200 > HAL_GetTick()){
    return;
  }
  last_interrupt = HAL_GetTick();

  if(state == 0){ //welcome
    state = 1;
    display_on_screen();
  }
  else if(state == 1){ //enter pass
    if(check_for_pass_correctness()){
      enteredPass[passIndex] = prevVolume - currVolume;
      prevVolume=currVolume;
      passIndex++;
      if(passIndex >= 4){
        state = 2;
      }
      display_on_screen();
    }
    else {
      state = 3;
      PWM_Start();
	  Change_Melody(error, ARRAY_LENGTH(error));
      display_on_screen();
    }
  }
  else if(state == 2){ //success
    state = 0;
    display_on_screen();
  }
  else if(state == 3){ //error
    state = 1;
    display_on_screen();
  }
}

void HAL_SYSTICK_Callback(){
	Update_Melody();
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
  if (htim->Instance == TIM2){
		if(state == 1){
			int x = HAL_ADC_GetValue(&hadc2);
			float fx=((float)x*95/4095);
			int value = normalizeVolume(fx);
			if(value != currVolume){
				PWM_Start();
			    Change_Melody(bigh, ARRAY_LENGTH(bigh));
			}
			currVolume = value;
			update_pass_live();
}
  }
  if(htim->Instance == TIM3){
	switch (state){
		case 0:
			if(animation == 0){
				scrollDisplayLeft();
				animation = 1;
			}else {
				scrollDisplayRight();
				animation = 0;
			}
			break;
		case 2:
			setCursor(18, 1);
			if(animation == 0){
				write(0);
				animation = 1;
			}else {
				write(1);
				animation = 0;
			}
			break;
		case 3:
			if(animation == 0){
				setCursor(5,1);
				print("          ");
				setCursor(5,2);
				print("          ");
				animation = 1;
			}else {
				setCursor(5, 1);
				print("Wrong pass");
				setCursor(5, 2);
				print("Try again!");
				animation = 0;
			}
			break;
	}
  }
}


